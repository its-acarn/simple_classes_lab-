public class Calculator {

    public int add(int num_1, int num_2) {
        return num_1 + num_2;
    }
    public int subtract(int num_1, int num_2) {
        return num_1 - num_2;
    }
    public int multiply(int num_1, int num_2) {
        return num_1 * num_2;
    }
    public double divide(double num_1, double num_2) {
        return num_1 / num_2;
    }
}
